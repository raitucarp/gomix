type fontDisplay string

const (
	displayAuto     fontDisplay = "auto"
	displayBlock    fontDisplay = "block"
	displaySwap     fontDisplay = "swap"
	displayOptional fontDisplay = "optional"
)

const googleapisUrl = "https://fonts.googleapis.com/css"

type googleFont struct {
	kind    googleFontKind
	bold    bool
	italic  bool
	display fontDisplay
	subsets []string
	weight  int
	text    string
}



func (f *googleFont) FamilyName() string {
	return string(f.kind)
}

func (f *googleFont) queries() url.Values {
	queryParams := url.Values{}

	name := f.FamilyName()
	if f.weight > 0 {
		name = strings.Join([]string{name, strconv.Itoa(f.weight)}, ":")
	} else if f.bold {
		name = strings.Join([]string{name, "bold"}, ":")
	} else if f.italic {
		name = strings.Join([]string{name, "italic"}, ":")
	} else if f.bold && f.italic {
		name = strings.Join([]string{name, "bolditalic"}, ":")
	}

	if f.display != "" {
		queryParams.Add("display", string(f.display))
	}

	if len(f.subsets) > 0 {
		queryParams.Add("subset", strings.Join(f.subsets, ","))

	}

	if f.text != "" {
		queryParams.Add("text", f.text)

	}

	queryParams.Add("family", name)
	return queryParams
}

func (f *googleFont) Link() string {

	encodedQuery := f.queries().Encode()

	return fmt.Sprintf("%s?%s", googleapisUrl, encodedQuery)

}
func (f *googleFont) Bold() *googleFont {
	f.bold = true
	return f
}
func (f *googleFont) Italic() *googleFont {
	f.italic = true
	return f
}
func (f *googleFont) BoldItalic() *googleFont {
	f.bold = true
	f.italic = true
	return f
}

